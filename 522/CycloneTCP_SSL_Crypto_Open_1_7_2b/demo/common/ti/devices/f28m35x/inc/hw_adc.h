#ifndef __HW_ADC_H__
#define __HW_ADC_H__

//*****************************************************************************
// The following are defines for the ADC Result register offsets.
//*****************************************************************************
#define ADC_O_RESULT0                  0x00 // ADC Result 0
#define ADC_O_RESULT1                  0x02 // ADC Result 1
#define ADC_O_RESULT2                  0x04 // ADC Result 2
#define ADC_O_RESULT3                  0x06 // ADC Result 3
#define ADC_O_RESULT4                  0x08 // ADC Result 4
#define ADC_O_RESULT5                  0x0A // ADC Result 5
#define ADC_O_RESULT6                  0x0C // ADC Result 6
#define ADC_O_RESULT7                  0x0E // ADC Result 7
#define ADC_O_RESULT8                  0x10 // ADC Result 8
#define ADC_O_RESULT9                  0x12 // ADC Result 9
#define ADC_O_RESULT10                 0x14 // ADC Result 10
#define ADC_O_RESULT11                 0x16 // ADC Result 11
#define ADC_O_RESULT12                 0x18 // ADC Result 12
#define ADC_O_RESULT13                 0x1A // ADC Result 13
#define ADC_O_RESULT14                 0x1C // ADC Result 14
#define ADC_O_RESULT15                 0x1E // ADC Result 15

#endif // __HW_ADC_H__



